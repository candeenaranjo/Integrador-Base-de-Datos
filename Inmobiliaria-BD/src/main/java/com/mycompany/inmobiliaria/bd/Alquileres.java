    private void cargarTablaPropiedades() {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //bloqueamos la edicion de texto en toda la tabla
                return false;
            }
        };
        modelo.addColumn("ID Propiedad");
        modelo.addColumn("Dirección");
        modelo.addColumn("Tipo");
        modelo.addColumn("Zona");
        modelo.addColumn("Valor Mensual ($)");

        // Le asignamos este modelo vacio pero con titulos a la tabla
        tablaPropiedades.setModel(modelo);

        // consulta SQL (1 significa True/Disponible)
        String sql = "SELECT p.id_Prop, p.direccion_Prop, t.descripcionTipo, z.nombre_Z, p.valor_Prop "
                + "FROM propiedades p, tipos_propiedad t, zonas z "
                + "WHERE p.cod_Tipo_Prop = t.cod_Tipo_Prop "
                + "AND p.nro_Zona_Ubicada = z.nro_Z "
                + "AND p.en_Alquiler = 1";
        try {
            //conectamos a MariaDB usando tu clase Conexion
            Connection con = Conexion.getConexion();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            // Recorremos los resultados y agregamos filas a la tabla
            while (rs.next()) {
                Object[] fila = new Object[5];
                fila[0] = rs.getInt("p.id_Prop");
                fila[1] = rs.getString("p.direccion_Prop");
                fila[2] = rs.getString("t.descripcionTipo");
                fila[3] = rs.getString("z.nombre_Z");
                fila[4] = rs.getFloat("p.valor_Prop");

                modelo.addRow(fila);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar las propiedades: " + ex.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }

