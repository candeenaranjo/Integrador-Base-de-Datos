    private void btnCrearContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearContratoActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaPropiedades.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this,
                    "Por favor, seleccione una propiedad de la tabla antes de crear el contrato.",
                    "Atención",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Para continuar con el contrato se le pedira\n el ingreso del inquilino y del escribano");


            int idPropiedad = (int) tablaPropiedades.getValueAt(filaSeleccionada, 0);
            float valorMensual = (float) tablaPropiedades.getValueAt(filaSeleccionada, 4);

            JOptionPane.showMessageDialog(this, "Para continuar con el contrato se le pedira\n el ingreso del inquilino y del escribano");

            IngresoInquilino ventanaInquilino = new IngresoInquilino(idPropiedad, valorMensual);
            ventanaInquilino.setLocationRelativeTo(null);
            ventanaInquilino.setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_btnCrearContratoActionPerformed

